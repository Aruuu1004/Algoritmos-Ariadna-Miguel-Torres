#1. Realizar un programa que lea cuatro valores numéricos e informar su suma y promedio.

valor1=int(input("Ingrese el primer valor numérico: "))
valor2=int(input("Ingrese el segundo valor numérico: "))
valor3=int(input("Ingrese el tercer valor numérico: "))
valor4=int(input("Ingrese el cuarto valor numérico: "))
suma=valor1+valor2+valor3+valor4
promedio=suma/4
print("La suma de los valores es: ")
print(suma)
print("El promedio de los valores es: ")
print(promedio)