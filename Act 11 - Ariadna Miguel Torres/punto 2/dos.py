#2. Calcular el sueldo mensual de un operario conociendo la cantidad de horas trabajadas y el valor por hora.

cantidadHoras=int(input("Ingrese la cantidad de horas trabajadas al mes: "))
valorHora=int(input("Ingrese el valor por hora: "))
sueldo=cantidadHoras*valorHora
print("El sueldo mensual es: ")
print(sueldo)